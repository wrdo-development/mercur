function buildTsconfigAugment(): string {
  const payload = {
    compilerOptions: {
      paths: {
        "@medusajs/types": ["./.mercur/types.d.ts"],
        "@medusajs/types-original": ["./node_modules/@medusajs/types"],
      },
    },
  }
  return JSON.stringify(payload, null, 2) + "\n"
}
