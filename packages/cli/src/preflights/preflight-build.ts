import path from "path"
import fs from "fs/promises"
import { writeRouteTypes, DIST_DIR, ensureDir } from "@/src/codegen"

export async function preflightBuild(rootDir: string): Promise<void> {
  await writeRouteTypes(rootDir)

  const mercurDir = path.join(rootDir, DIST_DIR)
  await ensureDir(mercurDir)

  await fs.writeFile(
    path.join(mercurDir, "types.d.ts"),
    buildTypesShim(),
    "utf-8",
  )
  await fs.writeFile(
    path.join(mercurDir, "tsconfig.augment.json"),
    buildTsconfigAugment(),
    "utf-8",
  )
}

